                        <div class="quote">提要：在美国与在中国有一点是相同的，那就是在衣食住行中，住的消费所占的比重最大，要解决住的问题，一是买(buy)，二是租(rent)，三是转租(sublease)。单身的一般与别人合租，有家庭的单独租房，极少数夫妻俩都有收入的，而且准备在美国长期待下去的也买了房子。下面是在美国租房的一些经历和需要注意事项。
</div><p>　　</p><div align="left">&nbsp; 在<a href="http://168liuxue.com/country/intro.lx?id=28" target="_blank">美国</a>与在<a href="http://168liuxue.com/country/intro.lx?id=202" target="_blank">中国</a>有一点是相同的，那就是在衣食住行中，住的消费所占的比重最大，要解决住的问题，一是买(buy)，二是租(rent)，三是转租(sublease)。单身的一般与别人合租，有家庭的单独租房，极少数夫妻俩都有收入的，而且准备在美国长期待下去的也买了房子。下面是在美国租房的一些经历和需要注意事项。</div>
<div align="left"><br><b>一、寻找短期转租(sublease)</b></div><strong></strong>
<div align="left"><br>&nbsp; sublease在英文中的原意是自己租(房子)再转租(或转租一部分)给别人，在<a href="http://168liuxue.com/country/intro.lx?id=202" target="_blank">中国留学</a>生中，单身的两人或三人合租一套房子的情况很普遍，大家对房租水电煤气暖气的费用都平均分配，转租的和被转租的，费用权利义务都一样。</div>
<div align="left"><br>&nbsp; 转租也分两种，一种是短期的，一种是长期的。在新生入学的春秋两季，大量的中国学生和博士后是第一次来美国，这是<a href="http://168liuxue.com/" target="_blank">留学</a>生们最困难的时候，日常生活用品都不齐全，尤其是床,而且囊中羞涩什么也舍不得买，所以建议大家找短期转租的，因为此时也有许多中国留学生回国探亲结婚什么的，房子空着也是空着，就委托在美国的朋友把房子租出去，床和锅碗瓢盆什么的都随你用，房租还收的比较低，因为他(她)即使不用也得照样交房租。</div>
<div align="left"><br><b>二、寻找长期转租</b></div><strong></strong>
<div align="left"><br>&nbsp; 寻找长期转租最关键的一点是，找个好室友，关键是人品，还有生活习惯。找个有洁癖的，所以长期寻找前，一定要对将来的室友甚至邻居有充分的了解，你可以利用短期合租的时间去找长期转租的房子，或者请熟人帮忙去摸摸底细。</div>
<div align="left"><br><b>三、自己租房</b></div><strong></strong>
<div align="left"><br>&nbsp; 这里的租房指的是与房东签租约(lease)的情况，租房要看地段、交通、环境等等，不必细说，值得一提的是房东的情况，根据网友的经验，将房东分为三类：学校的房屋管理部门(Division of University Housing)、房屋出租公司和私人房东，下面一一说来。</div>
<div align="left"><br>&nbsp; 对于大学里学生和博士后，本学校的房屋管理部门所拥有的房屋是个不错的选择，其优点是：<br>1、地点离上班地点近;<br>2、房屋保养的好，每次旧用户搬出去后，学校的房屋管理部门都会派专业人员来打扫和维修;<br>3、物业管理不错，保修基本上第二天就有人来解决了;<br>4、由于大家都属于同一所大学，所以不需要背景调查;<br>5、有的学校的房屋管理部门，租房者在租约上不需要明确租房时间，只需要提前三个月通知房屋管理部门既可，对于离校时间不确定的博士后来说，这一优点尤其重要;<br>6、虽然一般学校的房屋管理部门不允许租房者找人一起合租，但是在这上面管理的并不严，所以别人找我合租或者我找别人合租，都是私下进行的，也没有人管。学校的房屋管理部门的缺点是，相对来说价格偏高，因为学校的房屋管理部门的运行不如出租房屋的公司高效，所以成本较高。</div>
<div align="left">
<div align="left"><br><b>四、转租</b></div>
<div align="left"><strong></strong>&nbsp;</div></div>
<div align="left">&nbsp; 当你签约下房子后，你可以一个人住或一家人住，也可以找一个或两个合租者一起住，对于找室友，一定要摸清对方的底细，道理和寻找长期转租的一样。在找合租者时，有几点一定要注意：1、合租者有稳定的收入，不会拖欠房租;2、合租者待的时间最好要足够长，不然每个月都要找新的合租者也是件很麻烦的事;3、如果合租者中途要搬走，让他(她)尽量提前通知你，你好有足够的时间找下一个合租者。也有的转租者让后来的合租者一起与房东重新签合同的，以防止以上三种情况的发生，但是有的房东(尤其是房东是房屋出租公司)不允许签合同者改变合同，或者私自再招合租者。</div>
<div align="left"><br>&nbsp; 对于刚刚到美国的单身同胞们来说，最好能先找个短期转租的房子，日常用品都暂时用转租者的;然后找个长期转租的房子，注意找个好的转租者;或者自己租房，再挑一个好的合租者。对于成家的同胞们，当然还是尽快自己租房方便一些。如果有可能在一个城市待上五年以上，经济条件又允许的话，尽快买房子更好(但是最好等有了良好的信用纪录以后，这样银行的贷款利息会低一些)，毕竟最近房子的升值比银行的存款利息高多了。<!--EndFragment--></div><p></p>
